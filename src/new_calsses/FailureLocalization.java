package new_calsses;

public class FailureLocalization {

	private int id;
	private String name;
	public FailureLocalization(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	
	
}
